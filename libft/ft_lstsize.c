#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*p;
	int		i;

	p = lst;
	if (p == NULL)
		return (0);
	i = 1;
	while (p->next != 0)
	{
		p = p->next;
		i++;
	}
	return (i);
}
